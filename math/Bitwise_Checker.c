/************************************************************
 * プログラム名: Bitwise Checker
 * 説明       : 入力された数値の特定のビットがセットされているかを判定する
 * パラメータ  : なし
 * 戻り値     : なし
 ************************************************************/
#include <stdio.h>
#define BIT5 0x20
#define BIT_F 0xF0

int main(void)
{
    int x = 0;
    int y = 0;
    int z = 0;

    printf("整数を入力してください\n");
    printf("x:");
    scanf("%x",&y);
    printf("y:");
    scanf("%x",&z);

    x |= (y & BIT5);
    x |= (z & BIT_F);

    // x に影響を与えるビットがセットされているか確認
    if(x == 0)
    {
        printf("xは影響を受けていません（全ビット0）\n");
    }
    else
    {
        printf("xは影響を受けています（少なくとも1つのビットが1）\n");
    }
        
    return 0;
}